public class StaticInputExample {
    public static void main(String[] args) {
        // Static array of inputs (you can also use other data structures like ArrayList, HashMap, etc.)
        String[] inputs = {"apple", "orange", "banana", "grapes", "mango"};
        
        // Displaying the static inputs
        System.out.println("Static inputs:");
        for (String input : inputs) {
            System.out.println(input);
        }
    }
}
